package com.model;

public class DiagnosticService {

	
	private int diagnosticServiceId;
	private String diagnosticServiceName;
	private String briefOnDiagnosticCenter;
	private String facilitiesAvailable;
	private String speciality;
	private String address;
	public int getDiagnosticServiceId() {
		return diagnosticServiceId;
	}
	public void setDiagnosticServiceId(int diagnosticServiceId) {
		this.diagnosticServiceId = diagnosticServiceId;
	}
	public String getDiagnosticServiceName() {
		return diagnosticServiceName;
	}
	public void setDiagnosticServiceName(String diagnosticServiceName) {
		this.diagnosticServiceName = diagnosticServiceName;
	}
	public String getBriefOnDiagnosticCenter() {
		return briefOnDiagnosticCenter;
	}
	public void setBriefOnDiagnosticCenter(String briefOnDiagnosticCenter) {
		this.briefOnDiagnosticCenter = briefOnDiagnosticCenter;
	}
	public String getFacilitiesAvailable() {
		return facilitiesAvailable;
	}
	public void setFacilitiesAvailable(String facilitiesAvailable) {
		this.facilitiesAvailable = facilitiesAvailable;
	}
	public String getSpeciality() {
		return speciality;
	}
	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
